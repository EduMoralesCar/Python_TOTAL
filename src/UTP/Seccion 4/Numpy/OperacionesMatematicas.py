import numpy as np

arrayA = np.array([1, 2, 3])
arrayB = np.array([4, 5, 6])

# Operaciones Aritméticas
suma = arrayA + arrayB
resta = arrayA - arrayB
multiplicacion = arrayA * arrayB
division = arrayA / arrayB

print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)

# Funciones Matemáticas
arr = np.array([1, 2, 3, 4, 5])
raiz_cuadrada = np.sqrt(arr)
seno = np.sin(raiz_cuadrada)
logaritmo = np.log(arr)
promedio = np.mean(arr)
suma_total = np.sum(arr)

print("\nRaíz Cuadrada:", raiz_cuadrada)
print("Seno:", seno)
print("Logaritmo:", logaritmo)
print("Promedio:", promedio)
print("Suma Total:", suma_total)


