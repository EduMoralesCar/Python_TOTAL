# Operadores
x = 6
y = 2
z= 9

# Operaciones Aritméticas
print(f"La suma de {x} y {y} es {x+y}")
print(f"La resta de {x} y {y} es {x-y}")
print(f"La multiplicación de {x} y {y} es {x*y}")
print(f"La divición de {x} y {y} es {x/y}")
print(f"La divición de {z} y {x} es {z/x}")
print()
#divicion al piso
print(f"La divición de {z} y {y} es {z/y}")
print(f"El residuo de {z} entre {y} es {z%y}")
print(f"La raiz cuadrada de {x} es {x**0.5}")
print()
#Redondeo
print(f"la raiz cuadrada de {y} es {y**0.5}")
resultado = y**0.5
print(f"Resultado = {resultado}")

